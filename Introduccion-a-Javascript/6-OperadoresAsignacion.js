/*
Estos operadores tal como dicen, asignan valores a las variables.
*/

let edad = 42; // El operador "=" le asigna el valor 25 a la variable edad

edad += 2; // Equivalente a:   edad = edad + 2;
edad -= 2; // Equivalente a:   edad = edad - 2;
edad *= 2; // Equivalente a:   edad = edad * 2;
edad /= 2; // Equivalente a:   edad = edad / 2;
edad %= 2; // Equivalente a:   edad = edad % 2;

